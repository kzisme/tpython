def day_num(day_name):
    if day_name == 0:
        return "Sunday"
    elif day_name == 1:
        return "Monday"
    elif day_name == 2:
        return "Tuesday"
    elif day_name == 3:
        return "Wednesday"
    elif day_name == 4:
        return "Thursday"
    elif day_name == 5:
        return "Friday"
    elif day_name == 6:
        return "Saturday"

